function detectDeviceLanguage(): Language {
  try {
    let locale = 'tr';
    if (Platform.OS === 'ios') {
      locale =
        NativeModules.SettingsManager?.settings?.AppleLocale ||
        NativeModules.SettingsManager?.settings?.AppleLanguages?.[0] ||
        'tr';
    } else if (Platform.OS === 'android') {
      locale = NativeModules.I18nManager?.localeIdentifier || 'tr';
    }
    const code = locale.substring(0, 2).toLowerCase();
    if (code === 'tr') return 'tr';
    if (code === 'kk') return 'kk';
    if (code === 'ar') return 'ar';
    if (code === 'ru') return 'ru';
    if (code === 'en') return 'en';
  } catch {}
  return 'tr';
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLang] = useState<Language>('tr');
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    (async () => {
      try {
        const stored = await getStoredLanguage();
        if (stored && SUPPORTED.includes(stored as Language)) {
          setLang(stored as Language);
        } else {
          setLang(detectDeviceLanguage());
        }
      } catch {
        setLang(detectDeviceLanguage());
      } finally {
        setIsLoading(false);
      }
    })();
  }, []);

  const setLanguage = async (lang: Language) => {
    setLang(lang);
    try {
      await setStoredLanguage(lang);
    } catch {
      // ignore
    }
  };

  const t = (key: string): string => {
    return translations[language][key] ?? key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, isLoading }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function getTranslation(lang: Language, key: string): string {
  return translations[lang][key] ?? key;
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used inside LanguageProvider');
  return ctx;
}

