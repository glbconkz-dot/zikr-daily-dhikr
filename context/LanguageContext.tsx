export function getDhikrTitle(dhikr: Dhikr, language: Language): string {
  switch (language) {
    case 'en':
      return dhikr.titleEn ?? '';
    case 'ru':
      return dhikr.titleRu ?? '';
    case 'ar':
      return dhikr.titleAr ?? '';
    case 'kk':
      return dhikr.titleRu ?? dhikr.titleEn ?? dhikr.title;
    case 'tr':
    default:
      return dhikr.title;
  }
}

export function getDhikrMeaning(dhikr: Dhikr, language: Language): string {
  return DHIKR_I18N[dhikr.id]?.[language]?.meaning ?? '';
}

export function getDhikrExplanation(dhikr: Dhikr, language: Language): string {
  return DHIKR_I18N[dhikr.id]?.[language]?.explanation ?? '';
}

export function getDhikrSearchText(dhikr: Dhikr, language: Language): string {
  const meaning = getDhikrMeaning(dhikr, language);
  return [
    getDhikrTitle(dhikr, language),
    getDhikrTransliteration(dhikr, language),
    meaning,
  ]
    .join(' ')
    .toLowerCase();
}

