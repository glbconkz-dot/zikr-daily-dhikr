const ids = [...content.matchAll(/'([a-z_0-9]+)':\s*\{/g)].map((m) => m[1]);
const broken = [];
for (const id of ids) {
  const re = new RegExp(
    `'${id}':\\s*\\{[\\s\\S]*?kk:\\s*\\{([\\s\\S]*?)\\n    \\},`
  );
  const m = content.match(re);
  if (!m) continue;
  const kk = m[1];
  if (forbidden.some((p) => kk.includes(p))) broken.push(id);
}
console.log('Broken:', broken.length);
